package object_create_pattern.factory_method;

public class Robot {
	String name;
	String logo;

	public void setName(String name) {
		this.name = name;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}

	public String getName() {
		return name;
	}

	public String getLogo() {
		return logo;
	}
}
